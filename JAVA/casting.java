public class casting {
    public static void main(String[] args) {
        //implicit casting
        double price =100.00;
        double finalprice = price + 18;
        System.out.println(finalprice);

        //explicit casting
        int price1 =100;
        int finalprice1 = price1 + (int)18.18;
        System.out.println(finalprice1);
    }
}
