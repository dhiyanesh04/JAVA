public class Loops {
    public static void main(String[] args) {
        //for loop
      for(int i=0;i<=10;i=i+1){
          System.out.println(i);
      } 
      //while loop
      int j=10;
      while(j >= 1){
        System.out.println(j);
        j=j-1;
      }
      //do while loop
      int k=10;
      do{
        System.out.println(k);
        k=k-1;
      }while(k >=1 );
    }
}
