public class maths {
    public static void main(String[] args) {
        int a=5;
        int b=8;
        //maximum number 
        System.out.println(Math.max(a,b));
        //minimum number
        System.out.println(Math.min(a,b));
        //random number
        System.out.println(Math.random());
        System.out.println((int)(Math.random()*10));
    }
}
